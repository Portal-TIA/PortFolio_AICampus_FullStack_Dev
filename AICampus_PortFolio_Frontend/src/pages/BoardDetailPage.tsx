import { useCallback, useEffect, useState } from "react"
// 2. React Router 도구: URL 주소에서 값을 꺼내거나(useParams), 다른 페이지로 이동(useNavigate)할 때 씁니다.
import { useParams, useNavigate } from "react-router-dom"
// 3. 백엔드 통신 함수와 데이터 타입들을 불러옵니다.
import {
  deleteBoardPost,
  getBoardPostById,
  type BoardPost, //  type을 붙이면 interface 역할만 한다.
  getComments,
  createComment,
  deleteComment,
  type CommentResponse
} from "../api/boardApi"

// 4. UI 컴포넌트(버튼, 뱃지, 표 등)를 불러옵니다. (shadcn/ui)
import { Button } from "@/components/ui/button"
import { Badge } from "@/components/ui/badge"
import { Table, TableBody, TableCell, TableHead, TableHeader, TableRow } from "@/components/ui/table"

// 5. [임시 데이터] 나중엔 서버에서 받아오게 수정해야 합니다.
const otherPosts = [
  { idx: 2, title: "Shadcn 진짜 편하네요", date: "2025-02-22" },
  { idx: 3, title: "게시판 만들어봤습니다", date: "2025-02-21" },
]

export default function BoardDetailPage() {

  // 1. URL에서 주소값 꺼내기: App.tsx에서 `<Route path="/board/:idx" ...>` 라고 정해뒀기 때문에 `idx`로 꺼낼 수 있다.
  const { idx } = useParams()
  // 2. 페이지 이동: 삭제 성공 후 목록('/') 이동
  const navigate = useNavigate();

  // [State 저장소] 컴포넌트의 상태(데이터)를 기억하는 변수
  const [post, setPost] = useState<BoardPost | null>(null)          // 진짜 게시글 데이터 (처음엔 빈 상자)
  const [comments, setComments] = useState<CommentResponse[]>([])   // 댓글 목록 (처음엔 빈 배열) <CommentResponse[]> 객체배열
  const [newComment, setNewComment] = useState("")                  // 새로 입력 중인 댓글 내용
  const [tempWriter, setTempWriter] = useState("익명유저")           // 댓글 작성자 이름 (기본값 설정)

  // [기능 1] 댓글 불러오기 함수
  // useCallback: 함수를 메모리에 저장해두고 재사용하는 최적화 도구입니다. 의존성 배열([idx]) 안의 값이 바뀔 때만 함수를 새로 만듭니다.
  const fetchComments = useCallback(async () => {
    if (idx) { // URL에 번호가 잘 들어왔다면
      try {
        const data = await getComments(Number(idx)); // 백엔드에 "이 번호 글의 댓글 줘!" 라고 요청 (문자를 숫자로 바꿔서)
        setComments(data);                           // 응답받은 데이터를 State 저장소에 보관
      } catch (error) {
        console.error("댓글을 불러오지 못했다!", error);
      }
    }
  }, [idx]) // [idx]가 바뀔 때마다 함수를 최신화함.

  // -----------------------------------------------------------------------------------------------
  // [기능 2] 화면이 처음 열릴 때 (또는 idx가 바뀔 때) 실행되는 함수
  // -----------------------------------------------------------------------------------------------
  useEffect(() => {
    // 백엔드에서 글과 댓글을 동시에 가져오는 비동기(async) 작업
    const loadData = async () => {
      if (!idx || idx === "undefined") return; // NaN 방어: 주소가 꼬여서 값이 이상하면 아예 통신을 멈춤

      try {
        const postData = await getBoardPostById(Number(idx)); // 1. 게시글 상세 내용 가져오기
        setPost(postData);                                    // 가져온 글을 State에 보관
        await fetchComments();                                // 2. 댓글 목록도 가져오기 (위에서 만든 함수 사용)
      } catch (error) {
        // 실패 시: 존재하지 않는 글이거나 서버가 죽었을 때
        console.error("데이터 로드 실패:", error);
        alert("게시글을 불러올 수 없습니다.");
        navigate("/"); // 사용자에게 알리고 메인으로 튕겨냄 (안전장치)
      }
    };

    loadData().catch(console.error); // 🟢 정의한 함수를 실행!
  }, [idx, fetchComments, navigate]); // 배열 안의 값들이 변하면 이 블록(useEffect)을 다시 실행하라는 뜻.

  // -----------------------------------------------------------------------------------------------
  // [기능 3] 게시글 삭제 처리 함수 (빨간 '삭제' 버튼 클릭 시)
  // -----------------------------------------------------------------------------------------------
  const handleDelete = async () => {
    if (!idx) return;

    // window.confirm: 브라우저 기본 팝업으로 사용자에게 한 번 더 묻는 방어 장치
    if (window.confirm("정말 이 게시글을 삭제할 것인가?")) {
      try {
        const result = await deleteBoardPost(Number(idx)); // 백엔드에 삭제 명령
        if (result === "deleted" || result) {              // 서버가 삭제 성공했다고 응답하면
          alert("삭제되었습니다.");
          navigate("/");                                   // 메인(게시판 목록)으로 이동
        }
      } catch (error) {
        alert("삭제 중 오류가 발생했습니다.");
      }
    }
  }

  // -----------------------------------------------------------------------------------------------
  // [기능 4] 새로운 댓글 등록 함수 ('등록' 버튼 클릭 시)
  // -----------------------------------------------------------------------------------------------
  const handleCommentSubmit = async () => {
    if (!newComment.trim()) { // .trim() : 공백 제거. (스페이스바만 치고 등록하는 걸 막음)
      alert("댓글 내용을 입력해주세요!");
      return;
    }
    try {
      // 1. 백엔드에 댓글 등록 명령 전송
      await createComment(Number(idx), { writer: tempWriter, content: newComment });

      // 2. 등록이 성공하면 입력창을 깨끗하게 비워줌
      setNewComment("");

      // 3. 댓글을 새로 작성했으니, 백엔드에서 댓글 목록을 처음부터 다시 불러와서 화면을 갱신함
      await fetchComments();
    } catch (error) {
      alert("댓글 등록에 실패했습니다.");
    }
  }

  // -----------------------------------------------------------------------------------------------
  // [기능 5] 특정 댓글 삭제 함수 (댓글 옆 '삭제' 버튼 클릭 시)
  // -----------------------------------------------------------------------------------------------
  const handleCommentDelete = async (commentIdx: number) => { // 삭제할 "댓글 번호"를 받아옴
    if (window.confirm("이 댓글을 삭제할까요?")) {
      try {
        await deleteComment(commentIdx); // 서버에 특정 댓글 삭제 요청
        alert("댓글이 삭제 되었습니다.");
        await fetchComments();           // 삭제 후 바뀐 목록으로 화면 갱신
      } catch (error) {
        alert("댓글 삭제에 실패했다.");
      }
    }
  }

  // -----------------------------------------------------------------------------------------------
  // [로딩 화면 처리]
  // 처음에 빈 상자(null)였던 post State에 백엔드 데이터가 도착하기 전까지 보여주는 화면입니다.
  // 이 코드가 없으면 데이터가 없는데 화면을 그리려다 에러(Cannot read property...)가 터집니다.
  // -----------------------------------------------------------------------------------------------
  if (!post) return <div className="p-10 text-center">데이터를 불러오는 중입니다...</div>

  // [UI 팁] 날짜가 너무 길게 들어오면(예: 2025-02-22T10:30:00) 앞의 10글자(YYYY-MM-DD)만 잘라서 예쁘게 보여줌
  const formattedDate = post.indate?.substring(0, 10) || "날짜 없음"


  return (
      <div className="flex flex-col gap-6 p-6 max-w-4xl mx-auto mt-6">

        {/* 상단: 카테고리, 제목, 작성자, 날짜, 조회수 헤더 영역 */}
        <div className="flex flex-col gap-2 border-b pb-4">
          <div className="flex items-center gap-2">
            <Badge variant="secondary">{post.category || "일반"}</Badge>
            {/* 데이터 바인딩: post 상자에서 꺼낸 진짜 제목 */}
            <h1 className="text-3xl font-bold tracking-tight">{post.title}</h1>
          </div>
          <div className="flex justify-between items-center text-sm text-muted-foreground mt-2">
            <div className="flex gap-4">
              <span className="font-medium text-foreground">작성자: {post.writer}</span>
              <span>날짜: {formattedDate}</span>
            </div>
            <span>조회: {post.views || 0}</span>
          </div>
        </div>

        {/* 중단: 게시글 본문 영역 (whitespace-pre-line으로 줄바꿈 인식) */}
        <div className="min-h-[200px] whitespace-pre-line leading-relaxed text-lg py-4">
          {post.content}
        </div>

        {/* 하단 버튼들: 목록이동, 수정, 삭제 */}
        <div className="flex justify-between border-t pt-6 mt-4">
          <Button variant="outline" onClick={() => navigate("/")}>목록으로</Button>
          <div className="flex gap-2">
            {/* 수정 페이지로 이동할 때 현재 글 번호(idx)를 꼬리표로 달아서 보냅니다. */}
            <Button variant="secondary" onClick={() => navigate(`/board/edit/${idx}`)}>수정</Button>
            <Button variant="destructive" onClick={handleDelete}>삭제</Button>
          </div>
        </div>

        {/* ========================================================= */}
        {/* 댓글 영역 시작 */}
        {/* ========================================================= */}
        <div className="flex flex-col gap-4 mt-8 border-t pt-8">
          <h2 className="font-semibold text-xl">댓글 {comments.length}개</h2>

          {/* 댓글 입력 폼 박스 */}
          <div className="flex flex-col gap-3 bg-muted/20 p-4 rounded-lg border">
            <div className="flex gap-2">
              {/* 작성자 이름 입력칸 */}
              <input className="w-24 rounded-md border px-3 py-2 text-sm bg-background shadow-sm"
                     value={tempWriter}
                     onChange={(e) => setTempWriter(e.target.value)}
                     placeholder="작성자" />
              {/* 댓글 내용 입력칸 */}
              <input className="flex-1 rounded-md border bg-background shadow-sm px-3 py-2 text-sm outline-none focus:ring-2 focus:ring-primary/50"
                     placeholder="이 게시글에 대한 생각을 남겨보세요."
                     value={newComment}
                     onChange={(e) => setNewComment(e.target.value)}
                     onKeyDown={(e) => { if (e.key === 'Enter') handleCommentSubmit() }} /> {/* 엔터 키 치면 바로 등록 */}
              <Button onClick={handleCommentSubmit} className="shadow-sm">등록</Button>
            </div>
          </div>
          {/* map(c) 배열의 요소 하나하나가 c에 들어온다. c자체가 객체라서 그 객체가 가진 속성에 .으로 접근 가능 */}
          {/* 댓글 목록 리스트 렌더링 */}
          <div className="flex flex-col gap-3 mt-2">
            {/* comments 배열을 순회하며 하나씩 div 블록으로 만들어 냄 */}
            {comments.map((c) => ( // 타입을 정의해놓음, interface 존재, 그래서 c를 따로 선언 안해도 추론기능을 사용함 c.idx, c.writer에 접근이 가능하다.
                <div key={c.idx} className="flex justify-between items-start bg-card border shadow-sm p-4 rounded-lg">
                  <div className="flex flex-col gap-2 w-full">
                    <div className="flex justify-between items-center">
                      <div className="flex items-center gap-3">
                        {/* 작성자 및 작성 날짜 바인딩 */}
                        <span className="font-bold text-sm text-foreground">{c.writer}</span>
                        <span className="text-xs text-muted-foreground">{c.indate ? String(c.indate).substring(0, 10) : "방금 전"}</span>
                      </div>
                      {/* 해당 댓글 번호(c.idx)를 넣어서 삭제 함수 호출 */}
                      <Button variant="ghost" size="sm" className="h-8 px-2 text-xs text-muted-foreground hover:text-destructive hover:bg-destructive/10" onClick={() => handleCommentDelete(c.idx)}>삭제</Button>
                    </div>
                    {/* 댓글 내용 바인딩 */}
                    <p className="text-sm text-foreground/90">{c.content}</p>
                  </div>
                </div>
            ))}

            {/* 댓글이 하나도 없을 때 보여주는 빈 상태 메시지 (Empty State 처리) */}
            {comments.length === 0 && <div className="text-center text-sm text-muted-foreground py-10 bg-muted/10 rounded-lg border border-dashed">첫 번째 댓글의 주인공이 되어보세요!</div>}
          </div>
        </div>

        {/* ========================================================= */}
        {/* 하단: 관련 카테고리 다른 글 리스트 (현재는 임시 더미 데이터 사용) */}
        {/* ========================================================= */}
        <div className="flex flex-col gap-4 mt-8 border-t pt-8">
          <h2 className="font-semibold text-xl">이 카테고리의 다른 글</h2>
          <div className="rounded-lg border shadow-sm overflow-hidden">
            <Table>
              <TableHeader>
                <TableRow><TableHead>제목</TableHead><TableHead className="w-32 text-right">등록일</TableHead></TableRow>
              </TableHeader>
              <TableBody>
                {/* otherPosts 더미 배열을 순회하여 화면에 표출 */}
                {otherPosts.map((p) => (
                    <TableRow key={p.idx} className="cursor-pointer hover:bg-muted/30" onClick={() => navigate(`/board/${p.idx}`)}>
                      <TableCell className="font-medium">{p.title}</TableCell>
                      <TableCell className="text-right text-sm text-muted-foreground">{p.date}</TableCell>
                    </TableRow>
                ))}
              </TableBody>
            </Table>
          </div>
        </div>
      </div>
  );
}






