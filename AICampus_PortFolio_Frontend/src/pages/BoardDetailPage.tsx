// id가 2,3 으로 하드코딩 되어있다. 이부분을 클릭하면 Maps ('/board/2')가 실행되므로 에러없이 정상이동
// NaN 에러가 발생하는 이유 : 이 상세 페이지 안에서 뭘 눌러서가 아니라 메인 페이지 ( Dashboard.tsx ) 에서 이 상세페이지로 넘어올때 주소가 꼬였기 때문
// id를 백엔드와 맞추어 idx로 수정! (NaN) 에러 방지
//----------------------------------------------------------------------------------------------




// 삭제 후에는 목록으로 돌아가야하므로 useNavigate가 필요
// [트러블 슈팅] BoardPost 는 /api/boardApi 에서 interface인데 변수처럼 import 할려고 하니 에러 발생 type 붙여라

//------------------------------------------------------------------------------
