//------------------------------------------------------------------------------
// 1. 의존성 배열 최적화: fetchComments에 idx 추가하여 최신 데이터 참조 보장
// 2. UX 및 안정성 증가: 삭제 성공/실패 alert 피드백 추가, 빈 데이터(undefined) 방어 코드(|| "일반") 추가
// 3. UI 폴리싱: 여백, 글자 크기, 테두리 스타일(shadow-sm 등) 추가하여 가독성 향상
// 4. 비동기 순서 보장: 댓글 작성/삭제 후 await fetchComments()를 통해 목록 즉각 갱신
// 5. IDE 경고 해결: useEffect 안에서 loadData().catch(console.error) 처리
// 6. NaN 에러 방지: 파라미터와 더미 데이터를 모두 'idx'로 통일하여 백엔드 DTO와 완벽 매칭
//------------------------------------------------------------------------------



 // 💡 드디어 컴포넌트가 여기서 올바르게 끝납니다!

//------------------------------------------------------------------------------------------------------------
// [학습 노트 요약] ... 생략 (아래쪽에 기존 주석들 유지)




//----------------------------------------------------------------------------------------------
// 오류는 아니지만, useEffect와 비동기 (async) 함수의 궁합때문에 발생하는 Lint 경고
// useEffect는 반환값으로 아무거도 없거나 청소영 함수만 기대한다.
// 하지만 async 함수인 loadData는 호출되는 순간 무조건 Promise 객체를 반환한다.
// 무시해도 기능상 문제는 없지만 깔끔하게 보이려면 함수를 호출만 하고 결과를 기다리지 않는다는 것을 명시하거나 에러 핸들링 추가
//----------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------
// .then() : 데이터 로딩이 성공하면 실행하라
// .then()을 붙여주면 "이 작업이 끝나면 여기 적힌 일을 할것이다" 리액트에게 알림
//  r : 백엔드에서 받아온 결과값 (Result)를 담는 변수 (지금은 loadData 내부에서 이미 setPost를 하고 있으므로 r은 보통 비어있거나 무시해도 된다.)
//
// .then()만 쓰는 것보다 .catch() 까지 붙여주는게 더 완벽한 코드이다. 네트워크 에러나 서버가 죽었을 때 앱이 터지지 않게 방어막을 치기 때문이다.

// try-catch는 선택이 아닌 생명줄이다.
// 네트워크 통신 (API 호출)은 언제나 실패할 가능성이 있다. 서버 점검중일수 있고, 인터넷이 끊길 수도 있고, 누군가 그새 게시글을 삭제했을 수 있다.
// 에러가 나더라도 catch 블록이 낚아채서 사용자에게 알림 alert를 주고 메인페이지로 안전하게 대피 Maps("/") 시킬수 있다.
// .then() 방식과 try-catch는 같은 역할을 한다. try-catch가 더 표준적인 방법이다. 이를 삭제하고 싶으면 호출하는 쪽에서 반드시 에러를 잡아야 한다.
//----------------------------------------------------------------------------------------------



//----------------------------------------------------------------------------------------------
// 메인 페이지(Dashboard.tsx)의 실수: 메인 게시판 목록에서 글을 클릭할 때, 스프링이 준 이름표인 post.idx를 써야 하는데 실수로 post.id를 불렀습니다.

// 주소창 오염: post.id는 데이터가 없으니 undefined가 됩니다. 그래서 브라우저 주소창이 http://localhost:5173/board/undefined 로 넘어가 버립니다.

// 상세 페이지(BoardDetailPage.tsx)의 당황: 상세 페이지가 열리면서 주소창에 있는 글자를 가져오는데, 숫자 대신 "undefined"라는 글자가 들어옵니다.

